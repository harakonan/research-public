-- 疾患に対応する傷病コードのある医科レセプトのSEQ2_NOを持つTABLE INIT_SEQ
CREATE  TABLE   INIT_SEQ(SEQ2_NO) AS
SELECT DISTINCT
    SEQ2_NO
FROM
    CMDV_T_RCP_MED_SY
WHERE
    PRAC_YM         >=  &START_YM
AND PRAC_YM         <=  &END_YM
AND VLD_FLG         =   1
-- 疾患に応じて変更する
AND SKWD_NAME_CD IN ('8833881','8844446','2729002','8831286','2724007','2724023','2724031','2724012')
;
